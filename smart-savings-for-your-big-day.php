        <li class="my-bara-advice"> Create a meaningful celebration with a guest list focused on those who truly matter.</li>
        <li class="my-bara-advice"><b>Streamline Invitations</b> – Opt for digital invitations instead of printed ones to save on costs and add a modern, eco-friendly twist.</li>
