<!-- JavaScript to toggle FAQs visibility -->
<script>
    document.addEventListener("DOMContentLoaded", function () {
        const toggleButton = document.getElementById("toggle-faqs");
        const hiddenFaqs = document.querySelector(".hidden-faqs");

        toggleButton.addEventListener("click", function () {
            if (toggleButton.getAttribute("data-state") === "collapsed") {
                // Show hidden FAQs
                hiddenFaqs.style.display = "block";
                toggleButton.textContent = "Show Less";
                toggleButton.setAttribute("data-state", "expanded");
            } else {
                // Hide hidden FAQs
                hiddenFaqs.style.display = "none";
                toggleButton.textContent = "Show More";
                toggleButton.setAttribute("data-state", "collapsed");
            }
        });
    });
</script>






